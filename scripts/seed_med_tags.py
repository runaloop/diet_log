def main():
    con = sqlite3.connect(DB)
    con.execute('PRAGMA foreign_keys = ON')
    gids = dict(con.execute('SELECT name, id FROM food_group').fetchall())
    missing, tagged = [], 0
    for name, prep, review, pairs in TAGS:
        row = con.execute('SELECT id FROM product WHERE name = ?', (name,)).fetchone()
        if not row:
            missing.append(name)
            continue
        pid = row[0]
        con.execute('UPDATE product SET prep_effort = ?, review = ? WHERE id = ?',
                    (prep, review, pid))
        for grp, w in pairs:
            con.execute(
                """INSERT INTO product_group(product_id, group_id, weight) VALUES (?,?,?)
                   ON CONFLICT(product_id, group_id) DO UPDATE SET weight=excluded.weight""",
                (pid, gids[grp], w))
        tagged += 1
    con.commit()
    left = con.execute('SELECT COUNT(*) FROM product WHERE review = 1').fetchone()[0]
    total = con.execute('SELECT COUNT(*) FROM product').fetchone()[0]
    con.close()
    print(f'tagged {tagged} | review-flagged: {left}/{total}')
    if missing:
        print('НЕ НАЙДЕНЫ (имя не совпало):')
        for m in missing:
            print(f'  {m}')


if __name__ == '__main__':
    main()
